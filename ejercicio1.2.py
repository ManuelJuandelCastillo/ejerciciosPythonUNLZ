print("Programa para calcular area y perimetro de un rectangulo")
print("************************************************************************\n")
base = float(input("Ingrese la base del rectangulo en metros: "))
altura = float(input("Ingrese la altura del rectangulo en metros: "))

print("El perimetro del rectangulo es: ", 2*base + 2*altura)
print("El area del rectangulo es: ", base * altura)